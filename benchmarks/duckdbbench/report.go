package duckdbbench

import (
	"fmt"
	"sort"
	"strings"
)

func fmtBytes(n int64) string {
	if n <= 0 {
		return "n/a"
	}
	const (
		kiB = 1 << 10
		miB = 1 << 20
		giB = 1 << 30
	)
	switch {
	case n >= giB:
		return fmt.Sprintf("%.2f GiB", float64(n)/giB)
	case n >= miB:
		return fmt.Sprintf("%.2f MiB", float64(n)/miB)
	case n >= kiB:
		return fmt.Sprintf("%.2f KiB", float64(n)/kiB)
	default:
		return fmt.Sprintf("%d B", n)
	}
}

func fmtMeasuredBytes(n int64) string {
	if n < 0 {
		return "n/a"
	}
	if n == 0 {
		return "0 B"
	}
	return fmtBytes(n)
}

func fmtNS(ns float64) string {
	if ns <= 0 {
		return "n/a"
	}
	switch {
	case ns >= 1e9:
		return fmt.Sprintf("%.3f s", ns/1e9)
	case ns >= 1e6:
		return fmt.Sprintf("%.3f ms", ns/1e6)
	case ns >= 1e3:
		return fmt.Sprintf("%.3f us", ns/1e3)
	default:
		return fmt.Sprintf("%.1f ns", ns)
	}
}

func fmtRatio(competitorNS, storeNS float64) string {
	if competitorNS <= 0 || storeNS <= 0 {
		return "n/a"
	}
	return fmt.Sprintf("%.2fx", competitorNS/storeNS)
}

func fmtPayloadRatio(bytes, logical int64) string {
	if bytes <= 0 || logical <= 0 {
		return "n/a"
	}
	return fmt.Sprintf("%.2fx", float64(bytes)/float64(logical))
}

func fmtDigest(digest string) string {
	if len(digest) < 12 {
		return "n/a"
	}
	return "`" + digest[:12] + "…`"
}

func fmtMBps(bytes int64, ns float64) string {
	if bytes <= 0 || ns <= 0 {
		return "n/a"
	}
	return fmt.Sprintf("%.1f MiB/s", float64(bytes)/(1<<20)/(ns/1e9))
}

func profileRows(log *DuckDBLog, label string) string {
	if log == nil || len(log.Profiles[label]) == 0 {
		return "n/a"
	}
	profiles := log.Profiles[label]
	best := profiles[0]
	for _, profile := range profiles[1:] {
		if profile.Latency < best.Latency {
			best = profile
		}
	}
	return fmt.Sprintf("%d", best.CumulativeRowsScanned)
}

func verificationStatus(bad []string) string {
	if len(bad) == 0 {
		return "verified"
	}
	return "FAIL: " + strings.Join(bad, "; ")
}

// BuildReport renders a report from generated manifests and immutable raw
// artifacts. It never merges unlike memory categories into a synthetic ratio.
func BuildReport(res OursResults, logs map[string]DuckDBLog) string {
	var b strings.Builder
	w := func(format string, args ...any) { fmt.Fprintf(&b, format, args...) }

	corpora := append([]OursCorpus(nil), res.Corpora...)
	sort.Slice(corpora, func(i, j int) bool { return corpora[i].Manifest.Name < corpora[j].Manifest.Name })
	logFor := func(name string) *DuckDBLog {
		log, ok := logs[name]
		if !ok {
			return nil
		}
		return &log
	}

	w("# Store and DuckDB comparison\n\n")
	w("Generated by `duckdbbench`; see [the frozen methodology](../duckdbbench/duckdb-methodology.md). ")
	w("Every result is tied to one deterministic manifest and rejected when either engine disagrees with its expected counts.\n\n")

	w("## Environment\n\n")
	w("- Store: %s, %s/%s, one goroutine", res.GoVersion, res.GOOS, res.GOARCH)
	if res.Host != "" {
		w(", host: %s", res.Host)
	}
	w(".\n")
	versions := map[string]bool{}
	for _, log := range logs {
		versions[fmt.Sprintf("%s (%s), %s, threads=%d", log.Version, log.Image, log.Platform, log.Threads)] = true
	}
	if len(versions) == 0 {
		w("- DuckDB: no run artifacts; competitor cells are `n/a`.\n")
	} else {
		var values []string
		for value := range versions {
			values = append(values, value)
		}
		sort.Strings(values)
		for _, value := range values {
			w("- DuckDB: %s.\n", value)
		}
	}
	w("\n")

	w("## Correctness gate\n\n")
	w("| corpus | Store | DuckDB |\n|---|---|---|\n")
	for _, corpus := range corpora {
		storeStatus := verificationStatus(corpus.Store.Verify(corpus.Manifest))
		duckStatus := "not run"
		if log := logFor(corpus.Manifest.Name); log != nil {
			duckStatus = verificationStatus(log.Verify(corpus.Manifest))
		}
		w("| %s | %s | %s |\n", corpus.Manifest.Name, storeStatus, duckStatus)
	}
	w("\n")

	w("## Corpora\n\n")
	w("Logical payload is exact UTF-8 key bytes plus exact minified JSON bytes. NDJSON newlines are transport framing.\n\n")
	w("| corpus | class | rows | shapes | keys | JSON | key+JSON | NDJSON SHA-256 |\n")
	w("|---|---|---:|---:|---:|---:|---:|---|\n")
	for _, corpus := range corpora {
		m := corpus.Manifest
		w("| %s | %s | %d | %d | %s | %s | %s | %s |\n", m.Name, m.Class, m.Docs, m.ShapeCount,
			fmtBytes(m.KeyBytes), fmtBytes(m.SourceBytes), fmtBytes(m.LogicalBytes()), fmtDigest(m.NDJSONSHA256))
	}
	w("\n")

	w("## Storage and memory\n\n")
	w("These are deliberately separate accounting domains. Store is settled live Go heap. DuckDB file bytes are measured after `CHECKPOINT`; WAL is listed separately. DuckDB buffer and temporary peaks come from the engine's JSON profiler. Ratios in the two `payload` columns are each engine's own bytes divided by logical key+JSON bytes; there is no heap/disk cross-ratio.\n\n")
	w("| corpus | Store live heap | Store/payload | exact index model | DuckDB file | DuckDB/payload | WAL | peak buffers | peak temp |\n")
	w("|---|---:|---:|---:|---:|---:|---:|---:|---:|\n")
	for _, corpus := range corpora {
		m, store := corpus.Manifest, corpus.Store
		duckFile, wal, buffer, temp := int64(-1), int64(-1), int64(-1), int64(-1)
		if log := logFor(m.Name); log != nil {
			duckFile = log.Values["database_bytes"]
			wal = log.Values["wal_bytes"]
			buffer = log.PeakBufferBytes()
			temp = log.PeakTempBytes()
		}
		w("| %s | %s | %s | %s | %s | %s | %s | %s | %s |\n",
			m.Name, fmtBytes(store.HeapBytes), fmtPayloadRatio(store.HeapBytes, m.LogicalBytes()),
			fmtBytes(int64(store.IndexBytes)), fmtMeasuredBytes(duckFile), fmtPayloadRatio(duckFile, m.LogicalBytes()),
			fmtMeasuredBytes(wal), fmtMeasuredBytes(buffer), fmtMeasuredBytes(temp))
	}
	w("\n")

	w("## Load and index build\n\n")
	w("Both start from the same NDJSON. Store uses `Put`; DuckDB uses `read_ndjson_objects` into a `JSON` column plus materialized scalar columns. Index build is separate: Store backfills one exact path index; DuckDB builds single-column ART indexes for key and the comparable scalar path. Ratio is DuckDB/Store latency, so greater than 1 means Store completed sooner.\n\n")
	w("| corpus | Store load | Store rate | DuckDB load | DuckDB rate | load ratio | Store index | DuckDB indexes | index ratio |\n")
	w("|---|---:|---:|---:|---:|---:|---:|---:|---:|\n")
	for _, corpus := range corpora {
		m, store := corpus.Manifest, corpus.Store
		var duckLoad, duckIndex float64
		if log := logFor(m.Name); log != nil {
			duckLoad, _ = log.QueryNS("load")
			duckIndex = log.IndexNS()
		}
		w("| %s | %s | %s | %s | %s | %s | %s | %s | %s |\n", m.Name,
			fmtNS(float64(store.LoadNS)), fmtMBps(m.LogicalBytes(), float64(store.LoadNS)),
			fmtNS(duckLoad), fmtMBps(m.LogicalBytes(), duckLoad), fmtRatio(duckLoad, float64(store.LoadNS)),
			fmtNS(float64(store.IndexBuildNS)), fmtNS(duckIndex), fmtRatio(duckIndex, float64(store.IndexBuildNS)))
	}
	w("\n")

	w("## Warm reads\n\n")
	w("Ratio is DuckDB/Store latency. Point lookup returns one nested scalar by key. Filter, sum, group, and containment are whole-corpus operations. `rows scanned` is DuckDB's profiler field for the winning repetition; use the retained raw profiles and `EXPLAIN ANALYZE` when diagnosing plan changes.\n\n")
	w("| corpus | scenario | Store | DuckDB | ratio | DuckDB rows scanned |\n")
	w("|---|---|---:|---:|---:|---:|\n")
	for _, corpus := range corpora {
		m, store, log := corpus.Manifest, corpus.Store, logFor(corpus.Manifest.Name)
		rows := []struct {
			label string
			ours  int64
			run   bool
		}{
			{"point", store.PointNS, true},
			{"filter", store.FilterNS, m.ContainKey != ""},
			{"sum", store.SumNS, m.SumField != ""},
			{"group", store.GroupNS, m.ContainKey != ""},
			{"contain", store.ContainNS, m.ContainKey != ""},
		}
		for _, row := range rows {
			if !row.run {
				continue
			}
			var duckNS float64
			if log != nil {
				duckNS, _ = log.QueryNS(row.label)
			}
			w("| %s | %s | %s | %s | %s | %s |\n", m.Name, row.label,
				fmtNS(float64(row.ours)), fmtNS(duckNS), fmtRatio(duckNS, float64(row.ours)), profileRows(log, row.label))
		}
	}
	w("\n")

	w("## Per-key mutations\n\n")
	w("Each operation is an independent publication/transaction commit. The deterministic smoke updates up to 256 keys to the same valid JSON object while clearing the indexed scalar, then deletes those keys. This is intentionally not a bulk SQL-vs-loop comparison.\n\n")
	w("| corpus | operations | Store update/op | DuckDB update/op | ratio | Store delete/op | DuckDB delete/op | ratio |\n")
	w("|---|---:|---:|---:|---:|---:|---:|---:|\n")
	for _, corpus := range corpora {
		store, log := corpus.Store, logFor(corpus.Manifest.Name)
		var updateNS, deleteNS float64
		if log != nil {
			updateNS, _ = log.MeanNS("update")
			deleteNS, _ = log.MeanNS("delete")
		}
		w("| %s | %d | %s | %s | %s | %s | %s | %s |\n", corpus.Manifest.Name, store.MutationOps,
			fmtNS(float64(store.UpdateNSOp)), fmtNS(updateNS), fmtRatio(updateNS, float64(store.UpdateNSOp)),
			fmtNS(float64(store.DeleteNSOp)), fmtNS(deleteNS), fmtRatio(deleteNS, float64(store.DeleteNSOp)))
	}
	w("\n")

	w("## Interpretation boundaries\n\n")
	w("- Store timings are in-process Go calls. DuckDB timings are the engine-reported `latency` field and include SQL parsing, binding, optimization, and execution, but exclude container startup and result formatting.\n")
	w("- Both engines use one execution thread. Warmups precede profiled queries; reports use the minimum repetition on both read paths. Raw profile streams remain the audit artifact.\n")
	w("- DuckDB stores exact JSON physically as its `JSON`/`VARCHAR` representation and also materializes the filter and numeric fields. Store retains exact JSON plus its structural representation and exact index.\n")
	w("- DuckDB's ART index scan is eligible only for a single plain column and is intended for point or very selective predicates. A low-cardinality filter can remain a vectorized scan; the report exposes rows scanned instead of pretending the ART served it.\n")
	w("- DuckDB's compound and expression indexes are valid storage objects but are not eligible for ART index scans today, so this harness does not create a decorative compound index.\n")
	w("- DuckDB is an embedded analytical database with ACID transactions, checkpoint/WAL persistence, vectorized SQL, and snapshot isolation. Store has a narrower embedded JSON API and different durability/concurrency semantics. Results apply only to the listed operations.\n")
	w("- A checkpointed database file is not resident memory, and settled Go heap is not durable storage. Use each payload ratio for capacity planning in its own domain; do not divide one by the other.\n")

	return b.String()
}
